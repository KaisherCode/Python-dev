# Data types

# string

first_name = "Kaisher"
last_name = 'Dev'
print('My full name is =>', first_name, last_name)

print(type(first_name)) # output: str
# Integer

my_age = 21
print(f"I am {my_age} years old.")

print(type(my_age)) # output: int

# boolean

ls_single = True
print('ls_single =>', ls_single)

# Inputs

my_age = input("How old are you?")
print('my_age =>', my_age)
print(type(my_age)) # output: str
